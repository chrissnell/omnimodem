//! Feld Hell bitmap font + column extraction (`get_font_data`), ported verbatim.
//!
//! Port of fldigi's default transmit raster font FeldHell-12 and its column
//! reader (`fldigi/src/feld/`, upstream 4.1.23 @ 61b97f413). Hellschreiber is a
//! facsimile mode: a character is *painted* as a stream of pixel columns from a
//! bitmap font, never decoded to text on the wire. `FELDHELL` is fldigi's
//! default TX font (`progdefaults.feldfontnbr` default = 4 -> `feldhell_12`;
//! ref: configuration.h:989-991, feld.cxx:537).
//!
//! Bit-exact vs fldigi: [`FONT`] is the FeldHell-12 table transcribed verbatim
//! (ref: feld/FeldHell-12.cxx) and [`get_font_data`] reproduces feld.cxx:521-561
//! byte-for-byte. Both are asserted against the golden vector
//! `crates/dsp/tests/vectors/feldhell.json`
//! (`scratch/refvectors/build_feldhell.sh`).
//!
//! Column encoding: [`get_font_data`] returns a 14-bit value where bit `row`
//! (0..=13) is set iff pixel row `row` of that column is on. It returns `None`
//! when column `col` and every lesser column are blank — fldigi's "-1" signal
//! that the character is complete.

/// Number of pixel rows in a Feld Hell transmit column. ref: feld.h:42
/// (`FELD_COLUMN_LEN`).
