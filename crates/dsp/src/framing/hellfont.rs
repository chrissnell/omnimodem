/// Column bits for character `c`, column `col`, from [`FONT`]. Returns `None`
/// when the character is complete (fldigi's `-1`): column `col` is blank *and*
/// no set pixel remains in any lesser column. ref: feld.cxx:521-561.
pub fn get_font_data(c: u8, col: usize) -> Option<u16> {
    if col > 15 || !(FIRST_CHAR..=LAST_CHAR).contains(&c) {
        return None;
    }
    let glyph = &FONT[(c - FIRST_CHAR) as usize];
    let mask: u16 = 1 << (15 - col);

    // OR of all row words: nonzero bits that remain to the right gate completion.
    let mut ordbits: u16 = 0;
    for &row in glyph {
        ordbits |= row;
    }

    // Assemble this column: bit `row` set iff pixel (row, col) is on. fldigi
    // reads rows bottom-up (`byte[13 - row]`) so bit 0 is the bottom pixel.
    let mut bits: u16 = 0;
    for row in 0..COLUMN_ROWS {
        if glyph[COLUMN_ROWS - 1 - row] & mask != 0 {
            bits |= 1 << row;
        }
    }

    let testval: u16 = mask.wrapping_sub(1); // (1 << (15 - col)) - 1
    if bits == 0 && (ordbits & testval) == 0 {
        return None;
    }
    Some(bits)
}

/// The trimmed glyph raster for `c`: its column values left-to-right, stopping at
/// the first completion signal. Empty for a blank glyph (e.g. space). ref:
/// feld.cxx:644-655 (the `while get_font_data(...) != -1` loop).
pub fn glyph_columns(c: u8) -> Vec<u16> {
    let mut cols = Vec::new();
    let mut col = 0;
    while let Some(bits) = get_font_data(c, col) {
        cols.push(bits);
        col += 1;
    }
    cols
}

/// fldigi's default transmit width: each glyph column is scanned once. ref:
/// configuration.h:689-691 (`HellXmtWidth` default = 1).
pub const DEFAULT_XMT_WIDTH: usize = 1;

/// Append one character's on-air columns to `out`: a leading null column, then
/// either three more null columns (space) or the glyph's columns each repeated
/// `xmt_width` times, then a trailing null column. A "null column" is `0` (all
/// pixels off). ref: feld.cxx:633-659 (`tx_char`).
pub fn push_char_columns(out: &mut Vec<u16>, c: u8, xmt_width: usize) {
    out.push(0); // send_null_column (leading)
    if c == b' ' {
        out.extend([0, 0, 0]);
    } else {
        for bits in glyph_columns(c) {
            for _ in 0..xmt_width {
                out.push(bits);
            }
        }
    }
    out.push(0); // send_null_column (trailing)
}

/// The full on-air column raster for `text`: the bit-exact pixel-column stream a
/// Feld Hell transmitter puts on the air (each column is 14 rows, bit `row` on
/// iff that pixel is lit). This is the bit-domain TX golden reference; the audio
/// that carries it is gated separately (loopback only). ref: feld.cxx:661-715
/// (`tx_process` -> `tx_char`).
pub fn on_air_columns(text: &str, xmt_width: usize) -> Vec<u16> {
    let mut out = Vec::new();
    for &b in text.as_bytes() {
        // fldigi maps CR/LF to space and only transmits printable ASCII.
        let c = match b {
            b'\r' | b'\n' => b' ',
            _ => b,
        };
        push_char_columns(&mut out, c, xmt_width);
    }
    out
}

